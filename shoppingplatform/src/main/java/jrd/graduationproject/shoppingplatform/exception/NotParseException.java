package jrd.graduationproject.shoppingplatform.exception;

public class NotParseException extends RuntimeException {

	public NotParseException() {
		super();
	}

	public NotParseException(String message) {
		super(message);
	}

	public NotParseException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotParseException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

}