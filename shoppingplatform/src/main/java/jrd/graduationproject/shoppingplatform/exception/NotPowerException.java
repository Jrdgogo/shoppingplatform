package jrd.graduationproject.shoppingplatform.exception;

public class NotPowerException extends RuntimeException {

	public NotPowerException() {
		super();
	}

	public NotPowerException(String message) {
		super(message);
	}

	public NotPowerException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotPowerException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

}